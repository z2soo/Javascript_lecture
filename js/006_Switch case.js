"use strict";

/* <Switch case 조건문>
    if 조건문과 대체 가능
    
*/

let age = 30;
switch (age) { 
    case 2:
        console.log('아부부');
        break;
    case 20:
        console.log('술먹으러');
        break;
    case 30:
        console.log('아...');
        break;
    default:
        console.log('else(나머지)');
        break;
}   

